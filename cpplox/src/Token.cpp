#include <string>

#include "Token.hpp"

Token::Token(TokenType lType, std::string lLexeme, std::string lLiteral, unsigned int lLine)
    : mType(lType), mLexeme(lLexeme), mLiteral(lLiteral), mLine(lLine)
    {}


std::string Token::ToString()
{
    return mType + " " + mLexeme + " " + mLiteral;
}


